#include "ff.h"			/* Declarations of FatFs API */
#include "ffpriv.h"		/* Private declarations */

/* Check if chr is contained in the string */
int chk_chr (const char* str, int chr)	/* NZ:contained, ZR:not contained */
{
	while (*str && *str != chr) str++;
	return *str;
}
